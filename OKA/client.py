clients = [
    {"імя": "Діма", "сума угоди": 50, "статус перевірки": "clean"},
    {"імя": "Анна", "сума угоди": 250, "статус перевірки": "suspicious"},
    {"імя": "Олександр", "сума угоди": 1500, "статус перевірки": "fraud"},
    {"імя": "Катерина", "сума угоди": "помилка", "статус перевірки": "clean"},
    {"імя": "Андрій", "сума угоди": 700, "статус перевірки": "unknown"},
]

результат = []

for клієнт in clients:
    імя = клієнт.get("імя", "Без імені")
    сума = клієнт.get("сума угоди")
    статус = клієнт.get("статус перевірки", "").lower()

    if not isinstance(сума, (int, float)):
        категорія = "Фальшиві дані"
        рішення = "Ігнорувати"
    else:
        if сума < 100:
            категорія = "Дрібнота"
        elif сума < 1000:
            категорія = "Середнячок"
        else:
            категорія = "Великий клієнт"

        match статус:
            case "clean":
                рішення = "Працювати без питань"
            case "suspicious":
                рішення = "Перевірити документи"
            case "fraud":
                рішення = "У чорний список"
            case _:
                рішення = "Невідомий статус"

    результат.append({
        "імя": імя,
        "категорія": категорія,
        "рішення": рішення
    })

for r in результат:
    print(f"{r['імя']}: {r['категорія']} — {r['рішення']}")
